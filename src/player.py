# Write a class to hold player information, e.g. what room they are in
# currently.


class Player:
    def __init(room, health, damage):
        self.health = health
        self.damage = damage

        def defence(self, enemy):
            if self > 0 and enemy < 25:
                health += enemy

        def attack(self, enemy):
            if enemy > 0 and damage < 25:
                damage += enemy
